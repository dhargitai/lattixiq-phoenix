CREATE OR REPLACE FUNCTION "public"."match_knowledge_content_by_language"("query_embedding" "public"."vector", "match_threshold" double precision DEFAULT 0.5, "match_count" integer DEFAULT 5, "target_language" "text" DEFAULT 'English'::"text") RETURNS TABLE("id" "uuid", "title" "text", "category" "text", "type" "public"."knowledge_content_type", "summary" "text", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    knowledge_content.id,
    knowledge_content.title,
    knowledge_content.category,
    knowledge_content.type,
    knowledge_content.summary,
    1 - (knowledge_content.embedding <=> query_embedding) AS similarity
  FROM knowledge_content
  WHERE 
    knowledge_content.embedding <=> query_embedding < 1 - match_threshold
    AND knowledge_content.language = target_language
  ORDER BY knowledge_content.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;

