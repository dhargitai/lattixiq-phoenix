CREATE OR REPLACE FUNCTION "public"."match_knowledge_content_by_subcategory"("query_embedding" "public"."vector", "match_threshold" double precision DEFAULT 0.5, "match_count" integer DEFAULT 5, "target_language" "text" DEFAULT NULL::"text", "subcategories" "text"[] DEFAULT NULL::"text"[]) RETURNS TABLE("id" "uuid", "title" "text", "category" "text", "type" "public"."knowledge_content_type", "summary" "text", "similarity" double precision, "main_category" "text", "subcategory" "text")
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    kc.id,
    kc.title,
    kc.type,
    kc.definition,
    1 - (kc.embedding <=> query_embedding) AS similarity,
    kc.main_category::text,
    kc.subcategory::text
  FROM knowledge_content kc
  WHERE 
    kc.embedding <=> query_embedding < 1 - match_threshold
    AND (target_language IS NULL OR kc.language = target_language)
    AND (subcategories IS NULL OR kc.subcategory::text = ANY(subcategories))
  ORDER BY kc.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;

